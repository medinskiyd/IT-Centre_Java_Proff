public class Client {
    static Integer ownNumber;
    Integer receiverNumber;
    static String text;
    public Client(){

    }
    public Client(Integer ownNumber) {
        this.ownNumber = ownNumber;
    }

    public Client(Integer ownNumber, Integer receiverNumber) {
        this.ownNumber = ownNumber;
        this.receiverNumber = receiverNumber;
    }

    public Client(Integer receiverNumber, String text) {
        this.receiverNumber = receiverNumber;
        this.text = text;
    }

    public Integer getOwnNumber() {
        return ownNumber;
    }

    public void setOwnNumber(Integer ownNumber) {
        this.ownNumber = ownNumber;
    }

    public Integer getReceiverNumber() {
        return receiverNumber;
    }

    public void setReceiverNumber(Integer receiverNumber) {
        this.receiverNumber = receiverNumber;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Client(Integer ownNumber, Integer receiverNumber, String text) {
        this.ownNumber = ownNumber;
        this.receiverNumber = receiverNumber;
        this.text = text;
    }

    @Override
    public String toString() {
        return "Client{" +
                "ownNumber=" + ownNumber +
                ", receiverNumber=" + receiverNumber +
                ", text='" + text + '\'' +
                '}';
    }
}
