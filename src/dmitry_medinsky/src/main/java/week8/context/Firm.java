public class Firm {

    private String name = null;
    private Man director = null;

    public Firm() {

    }

    public Firm(Man director, String name) {
        this.director = director;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Man getDirector() {
        return director;
    }

    public void setDirector(Man director) {
        this.director = director;
    }

    @Override
    public String toString() {
        return "Firm{" +
                "name='" + name + '\'' +
                ", director=" + director +
                '}';
    }
}

